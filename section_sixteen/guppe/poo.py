"""
Programacao Orientada a Objetos - POO

- POO eh um paradigma de programacao que utiliza que mapeamento
de objetos do mundo real para modelos computacionais.

- Paradigma de programacao eh a forma/metodologia utilizada para
pensar/desenvolver sistemas.

Principais elementos da Orientacao a Objetos
- Classe -> Modelo do objeto do mundo real sendo representado computacionalmente;
- Atributo -> Caracteristicas do objeto.
- Metodo -> Comportamento do objeto (funcoes).
- Construtor -> Metodo especial utilizado para criar os objetos;
- Objeto -> Instancia da classe.
"""

numero = 10

print(numero)
print(type(numero))

nome = 'Geek'
print(nome)
print(type(nome))


class Produto:
    pass


ps4 = Produto()
print(ps4)
print(type(ps4))
